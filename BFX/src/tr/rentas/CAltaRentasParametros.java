    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        int r = this.validaCamposVacios();//llamamos al metodo validaCamposVacios
        if (r == 0)//entra si los campos no regresan un 1, quiere decir que no vienen vacios
        {
            //obtenemos la fecha del jDateChooser
            String sFechaRentaActual = new SimpleDateFormat("yyyy-MM-dd").format(jDFechaRentaActual.getDate());
            System.out.println(sFechaRentaActual);

            Conexion con = new Conexion();
            con.Conecta("localhost", "trajin", "root", "123456");//conexión con BD
            if (con.con != null)
            {
                String s = "null," + "null," + "null," + "" + jTRentaActual.getText() + "" + ",'" +sFechaRentaActual + "'";
                Querys q = new Querys();
                q.Insertar(con.con, "rentasparametros", s);

                try
                {
                    con.con.close();
                    Mensajes.exito(this);

                } catch (Exception ex)
                {
                    Mensajes.msj(this, "Error en DB... " + ex.getMessage());
                }
            }
            CancelarActionPerformed(evt);
        }
    }//GEN-LAST:event_AceptarActionPerformed

