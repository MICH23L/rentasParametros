    public int validaCamposVacios()
    {
        if (jTRentaActual.getText().equals(""))
        {
            Mensajes.falla(this, "El campo de Renta Actual no debe estar en blanco");
            jTRentaActual.requestFocus();
            return 1;
        }else if (jDFechaRentaActual.getDate() == null)
        {
            Mensajes.falla(this, "El campo de Fecha de Renta Actual no debe estar en blanco");
            jDFechaRentaActual.requestFocus();
            return 1;
        }
        return 0;
    }

