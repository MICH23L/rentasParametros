    private void RegistrarRentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarRentaActionPerformed
        // TODO add your handling code here:
        int r = this.validaCamposVacios();
        if (r == 0)
        {
            Conexion objConexion = new Conexion();
            objConexion.Conecta("localhost", "trajin", "root", "");
            if (objConexion.con != null)
            {
                String sCadena = ""+","+rentaAnteriorRentasParametros.getText()+","+"'"+fechaRentaAnteriorRentasParametros.getText()+"'"+","+ rentaActualRentasParametros.getText() +","+"'"+fechaRentaActualRentasParametros.getText()+"'";
                Querys objQuerys = new Querys();
                objQuerys.Insertar(objConexion.con, "rentasparametros", sCadena);

                try
                {
                    objConexion.con.close();
                    Mensajes.exito(this);

                } catch (Exception ex)
                {
                    Mensajes.msj(this, "Error en DB... " + ex.getMessage());
                }
            }
            CancelarRentaActionPerformed(evt);
        }
    }//GEN-LAST:event_RegistrarRentaActionPerformed

